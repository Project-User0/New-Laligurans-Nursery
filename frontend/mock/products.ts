export function getProductById(id: number) {
  return mockProducts.find((p) => p.id === id)
}

export function getProductBySlug(slug: string) {
  return mockProducts.find((p) => p.slug === slug)
}

export function getProductsByCategory(categoryId: number) {
  return mockProducts.filter((p) => p.categoryId === categoryId)
}

export function getFeaturedProducts() {
  return mockProducts.filter((p) => p.featured)
}

export function getBestsellerProducts() {
  return mockProducts.filter((p) => p.isBestseller)
}

export function getNewProducts() {
  return mockProducts.filter((p) => p.isNew)
}

export function getAirPurifyingProducts() {
  return mockProducts.filter((p) => p.airPurifying)
}

export function getPetFriendlyProducts() {
  return mockProducts.filter((p) => p.petFriendly)
}

export function searchProducts(query: string) {
  const searchTerm = query.trim().toLowerCase()
  if (!searchTerm) return mockProducts

  return mockProducts.filter((p) => {
    const haystacks = [p.name, p.slug, p.description, p.category, p.careInstructions].map((value) => value.toLowerCase())

    return haystacks.some((value) => {
      const words = value.split(/[^a-z0-9]+/).filter(Boolean)
      return words.some((word) => word.startsWith(searchTerm)) || value.startsWith(searchTerm)
    })
  })
}
